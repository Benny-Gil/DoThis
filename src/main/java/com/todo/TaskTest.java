package com.todo;

public class TaskTest {
    public static void main(String[] args) {
        TaskList taskList = new TaskList();
        Task task1 = new Task("Task 1", "Description 1", "2021-08-01", "Done");
        Task task2 = new Task("Task 2", "Description 2", "2021-08-02", "Done");
        Task task3 = new Task("Task 3", "Description 3", "2021-08-03", "Done");
        Task task4 = new Task("Task 4", "Description 4", "2021-08-04", "Done");
        Task task5 = new Task("Task 5", "Description 5", "2021-08-05", "Done");
        taskList.addTask(task1);
        taskList.addTask(task2);
        taskList.addTask(task3);
        taskList.addTask(task4);
        taskList.addTask(task5);

        System.out.println("Task List:");
        for (int i = 0; i < taskList.getSize(); i++) {
            System.out.println(taskList.getTask(i).getTaskName());
        }
        System.out.println("Task List after removing task 3:");
        taskList.removeTask(2);
        for (int i = 0; i < taskList.getSize(); i++) {
            System.out.println(taskList.getTask(i).getTaskName());
        }
        System.out.println("Task List after editing task 2:");

        //edit task 2
        taskList.editTask(1, new Task("Task 2 edited", "Description 2 edited", "2021-08-02", "Done"));
        for (int i = 0; i < taskList.getSize(); i++) {
            System.out.println(taskList.getTask(i).getTaskName());
        }


    }
}
